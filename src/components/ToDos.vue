<template>
  <div>
    <h3>TO DOS</h3>
    <div class="todos">
      <div @click="toggleTodoCompleted(todo)" v-for="todo in allTodos" :key="todo.id" class="todo">
        {{ todo.title }}
        <i class="fas fa-trash-alt" @click="deleteTodo(todo.id)"></i>
      </div>
    </div>
  </div>
</template>

<script>

import {mapGetters, mapActions} from 'vuex'

export default {
    name: "TodosComp",
    methods: {
        ...mapActions(['fetchTodos','deleteTodo', 'toggleTodoCompleted'])
    },
    computed: {
        ...mapGetters(['allTodos']) 
    },
    created() {
        this.fetchTodos();
    }

}
</script>

